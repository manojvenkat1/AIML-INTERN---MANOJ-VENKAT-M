import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns
import numpy as np

df = pd.read_csv('Titanic-Dataset.csv')

print("Summary Statistics:")
print(df.describe())
print("\n")

numeric_features = df.select_dtypes(include=np.number).columns.tolist()

print("Generating Histograms and Boxplots...")
for feature in numeric_features:
    plt.figure(figsize=(12, 5))


    plt.subplot(1, 2, 1)
    sns.histplot(df[feature].dropna(), kde=True)
    plt.title(f'Histogram of {feature}')
    plt.xlabel(feature)
    plt.ylabel('Frequency')


    plt.subplot(1, 2, 2)
    sns.boxplot(y=df[feature].dropna())
    plt.title(f'Boxplot of {feature}')
    plt.ylabel(feature)

    plt.tight_layout()
    plt.show()

plt.figure(figsize=(10, 8))
correlation_matrix = df[numeric_features].corr()
sns.heatmap(correlation_matrix, annot=True, cmap='coolwarm', fmt=".2f")
plt.title('Correlation Matrix of Numeric Features')
plt.show()

selected_for_pairplot = [col for col in numeric_features if col not in ['PassengerId']]
if len(selected_for_pairplot) > 1:
    sns.pairplot(df[selected_for_pairplot].dropna())
    plt.suptitle('Pairplot of Selected Numeric Features', y=1.02)
    plt.show()
else:
    print("Not enough numeric features (excluding PassengerId) to create a pairplot.")

